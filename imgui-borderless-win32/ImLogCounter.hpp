struct ImLogCounter
{
    ImGuiTextBuffer     Buf;
    ImGuiTextFilter     Filter;
    ImVector<int>       LineOffsets; // Index to lines offset. We maintain this with AddLog() calls.
    bool                AutoScroll;  // Keep scrolling if already at the bottom.
    std::map<uint64_t, uint64_t> msgCache;
    std::unordered_map<uint64_t, const char*> msgMap;
    std::chrono::time_point<std::chrono::high_resolution_clock> start;
    std::map<uint64_t, uint64_t> sortedMap;
    ImLogCounter()
    {
        start = std::chrono::high_resolution_clock::now();
        AutoScroll = true;
        Clear();
    }

    void    Clear()
    {
        Buf.clear();
        LineOffsets.clear();
        LineOffsets.push_back(0);
    }

    void AddMsg(uint64_t msg)
    {
        auto it = msgCache.find(msg);
        if (it != msgCache.end()) {
            msgCache[msg] = msgCache[msg] += 1;
        }
        else msgCache.insert({ msg, 1 });

        int old_size = Buf.size();
        BuildMsg(msg);
        Buf.append("\n");
        for (int new_size = Buf.size(); old_size < new_size; old_size++)
            if (Buf[old_size] == '\n')
                LineOffsets.push_back(old_size + 1);

        std::vector<std::pair<uint64_t, uint64_t>> vec(msgCache.begin(), msgCache.end());

        // Sort the vector by value in descending order
        std::sort(vec.begin(), vec.end(), [](const std::pair<uint64_t, uint64_t>& a, const std::pair<uint64_t, uint64_t>& b) {
            return a.second > b.second;
        });
        
        sortedMap.clear();

        for (const auto& p : vec) {
            sortedMap[p.second] = p.first;
        }
    }

    void BuildMsg(uint64_t msg)
    {
        std::string log;
        uint64_t us = std::chrono::duration_cast<std::chrono::microseconds>(std::chrono::high_resolution_clock::now() - start).count();
        auto it = msgMap.find(msg);
        if (it != msgMap.end()) log += it->second;
        else log += "UNKNOWN";
        
        Buf.append((std::to_string(us) + '\t' + log).c_str());
    }

    void SetMsgMap(const std::unordered_map<uint64_t, const char*>& msg2Str)
    {
        msgMap = msg2Str;
    }

    void    Draw(const char* title, bool* p_open = NULL)
    {
        if (!ImGui::Begin(title, p_open))
        {
            ImGui::End();
            return;
        }

        float avail_x = ImGui::GetContentRegionAvail().x;

        // Options menu
        if (ImGui::BeginPopup("Options"))
        {
            ImGui::Checkbox("Auto-scroll", &AutoScroll);
            ImGui::EndPopup();
        }

        // Main window
        if (ImGui::Button("Options"))
            ImGui::OpenPopup("Options");
        ImGui::SameLine();
        bool clear = ImGui::Button("Clear");
        ImGui::SameLine();
        bool copy = ImGui::Button("Copy");

        ImGui::Separator();

        ImGui::BeginGroup();

        Filter.Draw("Filter", avail_x * 0.65f);
        if (ImGui::BeginChild("scrolling", ImVec2(avail_x * 0.75f, 0), ImGuiChildFlags_None, ImGuiWindowFlags_HorizontalScrollbar))
        {
            if (clear)
                Clear();
            if (copy)
                ImGui::LogToClipboard();

            ImGui::PushStyleVar(ImGuiStyleVar_ItemSpacing, ImVec2(0, 0));
            const char* buf = Buf.begin();
            const char* buf_end = Buf.end();
            if (Filter.IsActive())
            {
                // In this example we don't use the clipper when Filter is enabled.
                // This is because we don't have random access to the result of our filter.
                // A real application processing logs with ten of thousands of entries may want to store the result of
                // search/filter.. especially if the filtering function is not trivial (e.g. reg-exp).
                for (int line_no = 0; line_no < LineOffsets.Size; line_no++)
                {
                    const char* line_start = buf + LineOffsets[line_no];
                    const char* line_end = (line_no + 1 < LineOffsets.Size) ? (buf + LineOffsets[line_no + 1] - 1) : buf_end;
                    if (Filter.PassFilter(line_start, line_end))
                        ImGui::TextUnformatted(line_start, line_end);
                }
            }
            else
            {
                // The simplest and easy way to display the entire buffer:
                //   ImGui::TextUnformatted(buf_begin, buf_end);
                // And it'll just work. TextUnformatted() has specialization for large blob of text and will fast-forward
                // to skip non-visible lines. Here we instead demonstrate using the clipper to only process lines that are
                // within the visible area.
                // If you have tens of thousands of items and their processing cost is non-negligible, coarse clipping them
                // on your side is recommended. Using ImGuiListClipper requires
                // - A) random access into your data
                // - B) items all being the  same height,
                // both of which we can handle since we have an array pointing to the beginning of each line of text.
                // When using the filter (in the block of code above) we don't have random access into the data to display
                // anymore, which is why we don't use the clipper. Storing or skimming through the search result would make
                // it possible (and would be recommended if you want to search through tens of thousands of entries).
                ImGuiListClipper clipper;
                clipper.Begin(LineOffsets.Size);
                while (clipper.Step())
                {
                    for (int line_no = clipper.DisplayStart; line_no < clipper.DisplayEnd; line_no++)
                    {
                        const char* line_start = buf + LineOffsets[line_no];
                        const char* line_end = (line_no + 1 < LineOffsets.Size) ? (buf + LineOffsets[line_no + 1] - 1) : buf_end;
                        ImGui::TextUnformatted(line_start, line_end);
                    }
                }
                clipper.End();
            }
            ImGui::PopStyleVar();

            // Keep up at the bottom of the scroll region if we were already at the bottom at the beginning of the frame.
            // Using a scrollbar or mouse-wheel will take away from the bottom edge.
            if (AutoScroll && ImGui::GetScrollY() >= ImGui::GetScrollMaxY())
                ImGui::SetScrollHereY(1.0f);
        }
        ImGui::EndChild();
        ImGui::EndGroup();
        ImGui::SameLine();
        ImGui::BeginChild("counters", ImVec2(0, 0), ImGuiChildFlags_Borders);
        if (ImGui::BeginTable("Counts", 2))
        {
            ImGui::TableSetupColumn("Msg");
            ImGui::TableSetupColumn("Count");
            ImGui::TableHeadersRow();
            ImGui::TableNextRow();
            int unknown = 0;
            
            for (auto entry = sortedMap.rbegin(); entry != sortedMap.rend(); entry++)
            {
                ImGui::TableSetColumnIndex(0);
                
                auto it = msgMap.find(entry->second);
                if (it == msgMap.end())
                {
                    ImGui::Text("UNKNOWN %d", unknown++);
                }
                else ImGui::Text(it->second);

                ImGui::TableSetColumnIndex(1);
                ImGui::Text("%s", std::to_string(entry->first).c_str());
                ImGui::TableNextRow();
            }
            
            ImGui::EndTable();
        }
        ImGui::EndChild();

        ImGui::End();
    }
};